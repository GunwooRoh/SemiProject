    private void addEvent() {
        t2Table.addMouseListener(new EventHandler());
        
    }
    
    class EventHandler extends MouseAdapter{
        
        @Override
        public void mouseClicked(MouseEvent e) {
            if(e.getSource()==t2Table){
                int row = t2Table.getSelectedRow(); //선택된 행
                
                Object objNo = t2Table.getValueAt(row, 0);
                int no = Integer.parseInt((String)objNo);
                
                try {
                    showByNo(no);
                } catch (SQLException ex) {
                    ex.printStackTrace();
                }
                
            }
        }
        
        private void showByNo(int no) throws SQLException { //번호로 조회해서 테이블에 선택된 행의 데이터를 tf에 뿌려주는 메서드
            //1
            //2
            MemberDTO dto = memberDao.selectByNo(no); //번호로 조회
            
            //3 결과처리
            String memberNo = Integer.toString(dto.getMemberNo());
            t2tfMemberNo.setText(memberNo);
            t2tfUserid.setText(dto.getId());
            t2tfPwd.setText(dto.getPwd());
            t2tfName.setText(dto.getName());
            
            String gender = dto.getGender();
            if((gender.indexOf("남"))!=-1){
                t2rbMale.setSelected(true);
            }else if((gender.indexOf("여"))!=-1){
                t2rbFemale.setSelected(true);
            }
            
            String hp = dto.getHp();
            String hp1 = hp.substring(0, 3);
            String hp2 = hp.substring(4, 8);
            String hp3 = hp.substring(9);
            
            t2tfHp1.setText(hp1);
            t2tfHp2.setText(hp2);
            t2tfHp3.setText(hp3);
            
        }
        
    }
    
    //회원관리탭 테이블에 전체 회원 정보 뿌리기 - 조상혁
    private void showAll() throws SQLException {
        //1
        //2
        
        List<MemberDTO> list = memberDao.showMemberAll();
        
        //3
        String[] colNames = {"회원번호", "ID", "이름", "성별", "생년월일", "전화번호"};
        String[][] data = new String[list.size()][colNames.length];
        for(int i = 0; i < list.size(); i++){
            MemberDTO dto = list.get(i);
            
            data[i][0] = dto.getMemberNo() + "";
            data[i][1] = dto.getId();
            data[i][2] = dto.getName();
            data[i][3] = dto.getGender();
            data[i][4] = dto.getBirth() + "";
            data[i][5] = dto.getHp();
        }
        model.setDataVector(data, colNames);
        t2Table.setModel(model);
    }
    
